package com.larten.sep_project;

public class MonHoc {
    int id;
    String Name;
    int Students;
    String Day;
    String Time;
    String Room;

    public MonHoc(int id, String name, int students, String day, String time, String room) {
        this.id = id;
        Name = name;
        Students = students;
        Day = day;
        Time = time;
        Room = room;
    }
}
